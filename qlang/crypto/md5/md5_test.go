package md5

import (
	"testing"
)

// -----------------------------------------------------------------------------

func Test(t *testing.T) {

	h1 := Hash(",", "a", "b", []byte("c"))
	h2 := Sumstr([]byte("a,b,c"))
	if h1 != h2 {
		t.Fatal("md5.Hash failed:", h1, h2)
	}
}

